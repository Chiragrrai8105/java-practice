class ladder {

	public static void main(String[] args) {
		int mark = 85;
		if (mark>=90)
		{
			System.out.println("A Grade");
		}
		else if (mark>=75)
		{
			System.out.println("B Grade");
		}
		else if (mark>=60)
		{
			System.out.println("C Grade");
		}
		 else if (mark>=45)
		{
			System.out.println("D Grade");
		}
		else 
		{
			System.out.println("FAIL");
		}
		System.out.println("Thank you");
	}
}	

		